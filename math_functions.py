import math

radius=float(input("Enter the radius of circle :- "))

circumference=2*math.pi*radius

area=math.pi*pow(radius,2)

print(f"the circumference is : {round(circumference,2)}cm ")
print(f"the area is : {round(area,2)}cm ")










